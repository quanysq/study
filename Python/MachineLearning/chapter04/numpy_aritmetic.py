import numpy as np

# 创建多维数组
myarray1 = np.array([[1, 2, 3],
                     [2, 3, 4],
                     [3, 4, 5]])
myarray2 = np.array([[11, 21, 31],
                     [21, 31, 41],
                     [31, 41, 51]])
print('向量加法运算：')
print(myarray1 + myarray2)
print('向量乘法运算：')
print(myarray1 * myarray2)